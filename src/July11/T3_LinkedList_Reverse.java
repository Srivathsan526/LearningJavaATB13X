package July11;

import java.util.LinkedList;

public class T3_LinkedList_Reverse {
    public static void main(String[] args) {
        LinkedList l1 = new LinkedList();
        l1.add(10);
        l1.add(20);
        l1.add(30);
        l1.add(40);
        System.out.println("Reversed List");
        System.out.println(l1.reversed());

    }
}
