package June17;

public class Lab002ForWhile {
    public static void main(String[] args) {
        int i = 0;
        while(i<10){
            System.out.println("Output for WHILE loop is = " + i);
            i++;

        }
        for(int j=0; j < 10; j++){
            System.out.println("Output for For loop is = " + j);
        }
    }
}
