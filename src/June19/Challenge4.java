package June19;

public class Challenge4 {
    public static void main(String[] args) {
        int i = 5;
        System.out.println("Original: " + i + " (returned i = 5)");
        System.out.println("Pre-Increment: " + ++i + " (returned i = 6)");
        System.out.println("Post-Increment: " + i++ + " (returned i = 7)");
        System.out.println("Pre-Decrement: " + --i + " (returned i = 6)");
        System.out.println("Post-Decrement: " + i-- + " (returned i = 5)");

    }
}
